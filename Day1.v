Fixpoint getMinimum{n:nat}(v:Vector.t nat (S n)) : nat * Vector.t nat n :=
match v with
|cons _ k 0 u => (k, u)
|cons _ k (S m) u => let (j,w) := getMinimum u in
                     match (Nat.ltb k j) with
                     |true => (k, u)
                     |false => (j, cons _ k _ w)
                     end
end.

Definition dist(n m:nat) : nat :=
 match (Nat.ltb n m) with
 |true => m - n
 |false => n - m
end.

Fixpoint getDists{n:nat}(u v:Vector.t nat n) : Vector.t nat n :=
match n, u, v with
|0, _, _ => nil _
|S m, _, _ => match getMinimum u, getMinimum v with
              |(j, u'), (k, v') => cons _ (dist j k) _ (getDists u' v')
              end
end.

